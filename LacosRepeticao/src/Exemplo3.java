/*
    Conta de 1 até 10
 */
public class Exemplo3 {
    public static void main(String[] args) {

        double i = 1; // declara uma váriavel chamda i como contador e inicia aé o valor 1

        while (i <= 10) {   //Faça enquanto i for menor ou igual a 10
            System.out.println(i); //Exibe o valor da váriavel i
            //i++; //Incrementa a variavel i
            i = i + 1;
        } //fim do Loop while
    } //fim do método main
} //fim a clase Exemplo3